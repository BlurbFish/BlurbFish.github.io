<h2>Readings</h2>
<p>
    <ul>
        <li>"Inclusive Physical Education: Promoting Health for a Lifetime" by David L. Porretta and Lauren J. Lieberman (2014)
This book offers suggestions for developing inclusive physical education environments, including tactics for changing activities and promoting inclusive social connections.</li>
<li>"Adapted Physical Education and Sport" by Joseph P. Winnick and David L. Porretta (2016)
This in-depth guide covers a wide range of topics related to adapted physical education, such as inclusive practises, evaluation methods, programme design, and encouraging active involvement for students of all abilities.</li>
<li>"Teaching Disability Sport: A Guide for Physical Educators" by Ronald W. Davis (2017)
In inclusive physical education settings, this manual focuses on teaching sport to people with disabilities. It offers useful advice on how to modify exercises, encourage skill growth, and build inclusive learning settings.</li>
<li>"Creating Inclusive PE: Classroom Activities for All Students" by Angela M. Lumpkin and Ellen W. Thompson (2017)
This resource provides a collection of inclusive physical education activities suitable for a range of abilities. It includes step-by-step instructions, modifications, and adaptations to ensure all students can participate and benefit from physical education.</li>
<li>"Including Everyone: Creating Music and Movement Lessons for Students with Autism" by Alice M. Hammel and Ryan M. Hourigan (2015)
This resource gives insightful information on inclusive practises for kids with autism in physical education, despite its emphasis on music and dance instruction. To encourage their engagement, it offers applicable tactics, lesson plans, and modifications. </ul></p>

<h2>Programmes, Workshops and Projects</h2>
<ul>
<li>Unified Sports: Unified Sports is a program developed by Special Olympics that promotes social inclusion through inclusive sports activities. It brings together students with and without disabilities to compete together on the same teams, fostering friendships and understanding.
<li>Project UNIFY: Project UNIFY is an inclusive program created by Special Olympics that focuses on promoting social inclusion and reducing bullying in schools through sports, leadership, and advocacy activities. It provides resources and training for educators to create inclusive environments and empower students to become agents of change.
<li>Inclusive Fitness: Inclusive Fitness programs aim to create accessible and inclusive fitness opportunities for individuals of all abilities. These programs focus on adapting exercises and equipment, providing appropriate support, and promoting physical activity for individuals with disabilities.
<li>Para-Sports Programs: Many countries have specific programs and initiatives dedicated to promoting para-sports and inclusive physical activities for individuals with disabilities. These programs provide opportunities for students with disabilities to participate in adapted sports, competitions, and recreational activities.
Whole School, Whole Community, Whole Child (WSCC) Model: The WSCC Model is an inclusive approach to promoting health and well-being in schools. It emphasizes collaboration between school staff, families, and the community to create a supportive and inclusive environment that addresses the diverse needs of all students, including in physical education.
Adaptive Physical Education (APE) Programs: APE programs provide specialized physical education instruction and activities for students with disabilities. These programs focus on individualized instruction, adapting activities and equipment, and promoting motor skills development and physical fitness.</li></ul>



